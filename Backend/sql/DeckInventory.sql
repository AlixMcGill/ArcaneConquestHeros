CREATE TABLE DeckInventory (
    id SERIAL PRIMARY KEY,
    PlayerId INT NOT NULL,
    Name VARCHAR(30) NOT NULL,
    Description VARCHAR(300) NOT NULL,
    HeroIdOne INT NOT NULL,
    HeroIdTwo INT NOT NULL,
    HeroIdThree INT NOT NUll,
    HeroIdFour INT NOT NULL,
    HeroIdFive INT NOT NULL,
    HeroIdSix INT NOT NULL,
    HeroIdSeven INT NOT NULL,
    HeroIdEight INT NOT NULL,
    HeroIdNine INT NOT NULL,
    HeroIdTen INT NOT NULL,
    FOREIGN KEY(PlayerId) REFERENCES Users(id),
    FOREIGN KEY(HeroIdOne) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdTwo) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdThree) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdFour) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdFive) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdSix) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdSeven) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdEight) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdNine) REFERENCES HeroInventory(id),
    FOREIGN KEY(HeroIdTen) REFERENCES HeroInventory(id)
);
